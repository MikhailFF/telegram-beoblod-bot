@dataclass(frozen=True)
class BotReply:
    greeting: str
    emoji: str
    reaction: str
    joke: str
    advice: str
    closing: str

    def render(self) -> str:
        tail = random.choice([self.advice, self.closing, ""])
        parts = [f"{self.emoji} <b>{self.greeting}.</b>", self.joke]
        if tail:
            parts.append(tail)
        return "\n\n".join(parts)


def choose_emoji(text: str) -> str:
    lowered = text.lower()
    topic_emojis = [
        (("подлод", "флот", "море", "кораб", "вахт", "старпом"), "⚓"),
        (("лет", "авиа", "самолет", "пилот", "небо", "экипаж"), "✈️"),
        (("мед", "сан", "госпит", "диагноз", "пациент"), "🩺"),
        (("десант", "вдв", "спецназ"), "🪂"),
        (("артил", "стрел", "огонь"), "💥"),
        (("штаб", "приказ", "устав", "наряд"), "📋"),
        (("можно",), "😏"),
    ]
    for keywords, emoji in topic_emojis:
        if any(keyword in lowered for keyword in keywords):
            return emoji
    return random.choice(["🫡", "⚓", "💥", "🪖", "😏"])


def choose_rhyme_answer(user_text: str) -> str | None:
    lowered = user_text.lower()
    for trigger, answers in RHYME_ANSWERS.items():
        if re.search(rf"(?<!\w){re.escape(trigger)}(?!\w)", lowered, re.IGNORECASE):
            return random.choice(answers)
    return None


def build_reply(user_text: str = "") -> BotReply:
    rhyme_answer = choose_rhyme_answer(user_text)
    if rhyme_answer and random.random() < 0.65:
        joke = rhyme_answer
    elif re.search(r"\bможно\b", user_text, re.IGNORECASE):
        joke = random.choice(CAN_ANSWERS)
    else:
        joke = random.choice(JOKES)

    return BotReply(
        greeting=random.choice(GREETINGS),
        emoji=choose_emoji(f"{user_text} {joke}"),
        reaction=random.choice(REACTIONS),
        joke=joke,
        advice=random.choice(ADVICES),
        closing=random.choice(CLOSINGS),
    )


def normalize_username(username: str | None) -> str | None:
    if not username:
        return None
    return username.lower().lstrip("@")


def is_mentioned(message: Message, bot_username: str | None, bot_full_name: str | None = None) -> bool:
    username = normalize_username(bot_username)
    if not username or not message.text:
        return False

    for entity in getattr(message, "entities", None) or []:
        if entity.type not in {"mention", "text_mention"}:
            continue
        mention = message.text[entity.offset : entity.offset + entity.length]
        if normalize_username(mention) == username or mention.lower() == username:
            return True

    checks = [rf"(?<!\w)@{re.escape(username)}(?!\w)", rf"(?<!\w){re.escape(username)}(?!\w)"]
    if bot_full_name:
        checks.append(rf"(?<!\w){re.escape(bot_full_name)}(?!\w)")

    return any(re.search(pattern, message.text, flags=re.IGNORECASE) for pattern in checks)


async def post_init(app: Application) -> None:
    bot = await app.bot.get_me()
    app.bot_data["bot_username"] = bot.username
    app.bot_data["bot_full_name"] = bot.full_name
    LOGGER.info("Bot started as @%s", bot.username)


def is_private_update(update: Update) -> bool:
    return bool(update.effective_chat and update.effective_chat.type == ChatType.PRIVATE)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not update.effective_message or is_private_update(update):
        return
    await update.effective_message.reply_text(
        "Готов поднимать настроение. Тегни меня в чате, и я отвечу шуткой и советом."
    )


async def joke(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not update.effective_message or is_private_update(update):
        return
    await update.effective_message.reply_text(random.choice(JOKES))


async def advice(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not update.effective_message or is_private_update(update):
        return
    await update.effective_message.reply_text(random.choice(ADVICES))


async def ping(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not update.effective_message or is_private_update(update):
        return
    await update.effective_message.reply_text("На связи. Тегни меня через @username, и я отвечу шуткой и советом.")


async def reply_when_mentioned(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    message = update.effective_message
    if not message:
        return

    bot_username = context.bot_data.get("bot_username") or context.bot.username
    bot_full_name = context.bot_data.get("bot_full_name")
    if is_private_update(update):
        LOGGER.info("Ignoring private message %s", message.message_id)
        return

    LOGGER.info(
        "Received text in chat %s (%s): %r",
        message.chat_id,
        update.effective_chat.type if update.effective_chat else "unknown",
        message.text,
    )

    if not is_mentioned(message, bot_username, bot_full_name):
        return

    LOGGER.info("Replying in chat %s to message %s", message.chat_id, message.message_id)
    await message.reply_text(build_reply(message.text or "").render(), parse_mode=ParseMode.HTML)


def create_app(token: str) -> Application:
    app = Application.builder().token(token).post_init(post_init).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("joke", joke))
    app.add_handler(CommandHandler("advice", advice))
    app.add_handler(CommandHandler("ping", ping))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, reply_when_mentioned))
    return app


def main() -> None:
    load_dotenv()
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(name)s: %(message)s")
    logging.getLogger("httpx").setLevel(logging.WARNING)

    token = os.getenv("TELEGRAM_BOT_TOKEN")
    if not token:
        raise RuntimeError("Set TELEGRAM_BOT_TOKEN in .env or in the environment.")

    try:
        asyncio.get_running_loop()
    except RuntimeError:
        asyncio.set_event_loop(asyncio.new_event_loop())

    create_app(token).run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
